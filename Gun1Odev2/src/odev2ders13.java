
public class odev2ders13 {

	public static void main(String[] args) {
		// for
		for (int i = 2; i < 10; i += 2) {
			System.out.println(i);
		}
		System.out.println("For dongusu bitdi...");

		int i = 1;
		// while
		while (i < 10) {
			System.out.println(i);
			i++;
		}
		System.out.println("While dongusu bitdi");

		// do while
		int j = 100;
		do {
			System.out.println(j);
			j += 2;
		} while (j < 10);
		System.out.println("Do while dongusu bitdi");
	}

}
